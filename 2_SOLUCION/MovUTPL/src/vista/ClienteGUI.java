    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Dispositivo disp = new Dispositivo(
                txtMarca.getText(),
                txtModelo.getText(),
                txtNumero.getText()
        );

        Cliente cliente = new Cliente(
                txtNombres.getText(),
                txtCedula.getText(),
                txtCiudad.getText(),
                txtCorreo.getText(),
                disp
        );

        if (clienteControlador.crearCliente(cliente)) {
            cargarTablaClientes();
            limpiarCampos();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        int fila = tblClientes.getSelectedRow();
        if (fila != -1) {
            clienteSeleccionadoId = (int) tblClientes.getValueAt(fila, 0);
            txtNombres.setText(tblClientes.getValueAt(fila, 1).toString());
            txtCedula.setText(tblClientes.getValueAt(fila, 2).toString());
            txtCiudad.setText(tblClientes.getValueAt(fila, 3).toString());
            txtCorreo.setText(tblClientes.getValueAt(fila, 4).toString());
            txtMarca.setText(tblClientes.getValueAt(fila, 5).toString());
            txtModelo.setText(tblClientes.getValueAt(fila, 6).toString());
            txtNumero.setText(tblClientes.getValueAt(fila, 7).toString());
        }
    }//GEN-LAST:event_tblClientesMouseClicked

    private void btbActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbActualizarActionPerformed
        if (clienteSeleccionadoId == -1) {
            return;
        }

        Dispositivo disp = new Dispositivo(
                txtMarca.getText(),
                txtModelo.getText(),
                txtNumero.getText()
        );

        Cliente cliente = new Cliente(
                txtNombres.getText(),
                txtCedula.getText(),
                txtCiudad.getText(),
                txtCorreo.getText(),
                disp
        );
        cliente.setId(clienteSeleccionadoId);

        if (clienteControlador.actualizarCliente(cliente)) {
            cargarTablaClientes();
            limpiarCampos();
        }
    }//GEN-LAST:event_btbActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (clienteSeleccionadoId != -1) {
            clienteControlador.eliminarCliente(clienteSeleccionadoId);
            cargarTablaClientes();
            limpiarCampos();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

