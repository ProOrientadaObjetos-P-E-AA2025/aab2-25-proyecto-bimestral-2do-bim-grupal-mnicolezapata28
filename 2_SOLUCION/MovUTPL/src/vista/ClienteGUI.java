    private void cargarTablaClientes() {
        List<Cliente> lista = clienteControlador.listarClientes();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new String[]{"ID", "Nombres", "Cédula", "Ciudad", "Correo", "Marca", "Modelo", "Número"});

        for (Cliente c : lista) {
            Dispositivo d = c.getDispositivo();
            modelo.addRow(new Object[]{
                c.getId(), c.getNombres(), c.getCedulaPasaporte(),
                c.getCiudad(), c.getCorreo(),
                d.getMarca(), d.getModelo(), d.getNumero()
            });
        }

        tblClientes.setModel(modelo);
    }

